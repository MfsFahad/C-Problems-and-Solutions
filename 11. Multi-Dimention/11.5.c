#include <stdio.h>

int main()
{
    int n=2, i, j;

    int a[n][n], b[n][n], c[n][n];

    a[0][0] = 2;

    a[0][1] = 4;

    a[1][0] = 6;

    a[1][1] = 2;

    b[0][0] = 1;

    b[0][1] = 2;

    b[1][0] = 2;

    b[1][1] = 3;

    c[0][0] = (a[0][0] * b[0][0]) + (a[0][1] * b[1][0]);

    c[0][1] = (a[0][0] * b[0][1]) + (a[0][1] * b[1][1]);

    c[1][0] = (a[1][0] * b[0][0]) + (a[1][1] * b[1][0]);

    c[1][1] = (a[1][0] * b[0][1]) + (a[1][1] * b[1][1]);

    for(i=0; i<n; i++)
    {
        for(j=0; j<n; j++)
        {
            printf("%d ", c[i][j]);
        }

        printf("\n");
    }

    return 0;
}
